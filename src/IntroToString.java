public class IntroToString {

    public static void main(String args[])
    {
      // Assigning value to string by creating an object of String class

        String Name= new String ("Aniket"); // This line will assign Aniket value to String Name
        System.out.println(Name); // This will print Aniket

      // Assigning value to string direct ly similar to other data types

        String City= "Pune"; // This line will assign Pune value to String City
        System.out.println(City); // This will print City

    }

}
